{
	//Variaveis a serem utilizadas.
	BOOL OutMultithreadSuporte = FALSE;

	//Chama o método para realizar a operação.
	OutMultithreadSuporte = PonteiroTrabalho->GetMultithreadProtected();

	//Define no parametro de saida o valor
	Param_Out_MultithreadSuporte = OutMultithreadSuporte? true: false;
}

