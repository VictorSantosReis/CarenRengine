/// <summary>
/// (NotifyPreroll) - Notifica o coletor de mídia(ICarenMFMediaSink) que o relógio de apresentação está prestes a iniciar.
/// </summary>
/// <param name="Param_ProximaHoraInicio">A próxima hora de início para o relógio de apresentação, em unidades 100 nanosegundos. Este tempo é o mesmo valor que será dado ao método 
/// (ICarenRelogioApresentação.Start) quando o relógio de apresentação é iniciado.</param>
CarenResult CarenMFMediaSinkPreroll::NotifyPreroll(Int64 Param_ProximaHoraInicio)
{
	//Variavel que vai ser retornada.
	CarenResult Resultado = CarenResult(ResultCode::ER_E_NOINTERFACE, false);

	//Variavel COM
	ResultadoCOM Hr = E_FAIL;

	//Chama o método para notificar o coletor de midia
	Hr = PonteiroTrabalho->NotifyPreroll(Param_ProximaHoraInicio);

	//Verifica o resultado da operação.
	if (Sucesso(Hr))
	{
		//Deixa o método continuar.
	}
	else
	{
		//O método falhou.
		Resultado.AdicionarCodigo(ResultCode::ER_FAIL, false);

		//Sai do método
		goto Done;
	}

	//Determina sucesso na operação
	Resultado.AdicionarCodigo(ResultCode::SS_OK, true);

Done:;
	//Retorna o resultado
	return Resultado;
}